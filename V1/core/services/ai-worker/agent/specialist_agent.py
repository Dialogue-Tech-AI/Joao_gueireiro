    async def _process_function_calls(
        self,
        intermediate_steps: List,
        attendance_id: str,
        client_phone: str,
        agent_result: Dict
    ) -> Optional[str]:
        """
        Process function calls executed by the specialist agent
        
        Returns:
            Last processed_output when has_output and is_sync; None otherwise
        """
        last_processed_output: Optional[str] = None
        try:
            from utils.postgres_client import pg_client
            
            if not pg_client.pool:
                logger.warning("PostgreSQL client not connected, skipping function call processing")
                return None
            
            try:
                for step in intermediate_steps:
                    if not step or len(step) < 2:
                        continue
                    
                    action = step[0]
                    tool_result = step[1]
                    
                    tool_name = None
                    if hasattr(action, 'tool'):
                        tool_name = action.tool
                    elif isinstance(action, dict) and 'tool' in action:
                        tool_name = action['tool']
                    
                    if not tool_name:
                        continue
                    
                    # Get function call config from agent_function_calls (new table)
                    config_row = await pg_client.fetchrow(
                        """
                        SELECT has_output, processing_method
                        FROM agent_function_calls
                        WHERE name = $1 AND is_active = true
                        """,
                        tool_name
                    )
                    
                    if not config_row:
                        logger.warning(f"Function call config not found for {tool_name}")
                        continue
                    
                    has_output = config_row.get('has_output', False)
                    is_sync = True  # agent_function_calls has no is_sync; default sync
                    processing_method = (config_row.get('processing_method') or 'RABBITMQ').upper()
                    
                    if processing_method == 'RABBITMQ' and self.function_call_processor:
                        result = await self.function_call_processor.process_function_call(
                            function_call_name=tool_name,
                            result=tool_result,
                            attendance_id=attendance_id,
                            client_phone=client_phone,
                            has_output=has_output,
                            is_sync=is_sync
                        )
                        
                        if has_output and is_sync and result and result.get('output'):
                            last_processed_output = result['output']
                    
            except Exception as e:
                logger.error(f"Error processing function calls: {e}", exc_info=True)
            
            return last_processed_output
        except Exception as e:
            logger.error(f"Error in _process_function_calls: {e}", exc_info=True)
            return None
