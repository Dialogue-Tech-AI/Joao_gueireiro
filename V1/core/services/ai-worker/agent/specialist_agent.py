def _should_fallback_model(exc: Exception) -> bool:
    name = exc.__class__.__name__
    msg = str(exc) or ""
    if name == "NotFoundError":
        return True
    if "model_not_found" in msg or "does not exist or you do not have access" in msg:
        return True
    return False

def _is_temperature_unsupported_error(exc: Exception) -> bool:
    """
    Detecta erro de temperatura não suportada (alguns modelos nano só aceitam temperatura padrão = 1.0).
    """
    name = exc.__class__.__name__
    msg = str(exc) or ""
    if name == "BadRequestError":
        if "temperature" in msg.lower() and ("does not support" in msg.lower() or "only the default" in msg.lower()):
            return True
    if "unsupported_value" in msg.lower() and "temperature" in msg.lower():
        return True
    return False

def _fallback_model(model: str | None) -> str | None:
    if not model:
        return model
    model_clean = model.strip()
    if model_clean == "gpt-4o-nano":
        return "gpt-4o-mini"
    if model_clean == "gpt-5-nano":
        return "gpt-4o-mini"  # Fallback para gpt-4o-mini também
    return model


class FragmentedResponse(BaseModel):
    """
    Structured output schema for fragmented responses
    """
    response: str = Field(description="The response that would be generated by the assistant.")
    fragments: List[str] = Field(description="All segments of the response generated by the assistant, dividing all the information. Never split times (13:00), dates, or monetary values across fragments.")
    
    model_config = {
        "extra": "forbid",
        "json_schema_extra": {
            "name": "fragmented_response",
            "strict": True
        }
    }


class SpecialistAgent:
    """
    Specialist Agent for specific domain
    Uses domain-specific prompt and only assigned function calls
    Shares memory with other agents
    """
    
    def __init__(
        self,
        specialist_config: Dict[str, Any],
        memory_manager,
        routing_service,
        agent_config_service: AgentConfigService,
        multi_agent_config_service: MultiAgentConfigService,
        function_call_processor: Optional[FunctionCallProcessor] = None
    ):
        self.specialist_config = specialist_config
        self.memory_manager = memory_manager
        self.routing_service = routing_service
        self.agent_config_service = agent_config_service
        self.multi_agent_config_service = multi_agent_config_service
        self.function_call_processor = function_call_processor
        
        # Initialize LLM with specialist's model and temperature
        self.llm = ChatOpenAI(
            model=specialist_config.get('model', 'gpt-4.1'),
            temperature=specialist_config.get('temperature', 0.7),
            streaming=False,
            api_key=settings.openai_api_key,
            timeout=OPENAI_HTTP_TIMEOUT,
            max_retries=0,
        )
        
        self.structured_llm = self.llm.with_structured_output(
            FragmentedResponse,
            method="function_calling"
        )
        
        # Tools will be loaded dynamically based on specialist's function_call_names
        self.tools: List[DynamicFunctionCallTool] = []
        self._tools_initialized = False
    
    async def initialize_tools(self):
        """Initialize tools assigned to this specialist"""
        if self._tools_initialized:
            return
        
        function_call_names = self.specialist_config.get('function_call_names', []) or []
        
        if not function_call_names:
            logger.info(f"Specialist '{self.specialist_config['name']}' has no function calls assigned")
            self.tools = []
            self._tools_initialized = True
            return
        
        logger.info(f"🔄 Loading {len(function_call_names)} function calls for specialist '{self.specialist_config['name']}'")
        
        # Load function calls data for assigned names only
        all_function_calls_data = await self.agent_config_service.get_active_function_calls_with_metadata()
        
        # Filter to only assigned function calls
        assigned_function_calls = [
            fc for fc in all_function_calls_data
            if fc['name'] in function_call_names
        ]
        
        if len(assigned_function_calls) < len(function_call_names):
            missing = set(function_call_names) - {fc['name'] for fc in assigned_function_calls}
            logger.warning(f"⚠️  Some function calls not found or inactive for specialist '{self.specialist_config['name']}': {missing}")
        
        # Create tools dynamically
        created_tools = []
        for fc_data in assigned_function_calls:
            try:
                tool = DynamicFunctionCallTool(
                    function_call_name=fc_data['name'],
                    description=fc_data['prompt'],
                    input_schema=fc_data.get('input_schema'),
                    required_fields=fc_data.get('required_fields') or [],
                    optional_fields=fc_data.get('optional_fields') or [],
                    agent_config_service=self.agent_config_service
                )
                created_tools.append(tool)
                logger.debug(f"✅ Created tool for specialist '{self.specialist_config['name']}': {fc_data['name']}")
            except Exception as e:
                logger.error(f"❌ Failed to create tool {fc_data['name']} for specialist '{self.specialist_config['name']}': {e}", exc_info=True)
        
        self.tools = created_tools
        self._tools_initialized = True
        tool_names = [t.name for t in self.tools] if self.tools else []
        logger.info(f"✅ Specialist '{self.specialist_config['name']}' initialized with {len(self.tools)} tools", extra={"tool_names": tool_names})
    
    async def format_system_message(self, chat_history: str, context: str) -> str:
        """Format system message with specialist prompt and context (legacy method)"""
        specialist_prompt = self.specialist_config.get('prompt', '')
        
        system_parts = [specialist_prompt]
        
        if context:
            system_parts.append(f"\nCONTEXTO ADICIONAL:\n{context}")
        
        if chat_history:
            system_parts.append(f"\nHISTÓRICO DA CONVERSA:\n{chat_history}")
        
        return "\n".join(system_parts)
    
    async def format_system_message_new_structure(
        self,
        recent_messages: str,
        intermediate_summary: str,
        rag_context: str,
        last_attendance_summary: str = "",
        open_cases: list = None,
        attendance_id: str = "",
        triagem_context: dict = None,
        operational_state: str | None = None,
        attendance_context: str | None = None,
    ) -> str:
        """Format system message with new context structure (SOMENTE contexto do sistema).
        
        IMPORTANTE: Não incluir prompt do painel aqui. O prompt do especialista e o prompt universal
        são concatenados externamente na montagem final do SYSTEM message, para termos separação
        determinística no execution_log.
        """
        system_parts = []
        
        # DATA E HORA ATUAL (Brasília) - SEMPRE presente para saudação e function calls (formato XML)
        try:
            ph = get_brasilia_placeholders()
            system_parts.append("")
            system_parts.append("<DataHoraAtual>")
            system_parts.append(f"  <Data>{ph.get('{{data_brasilia}}', 'N/A')}</Data>")
            system_parts.append(f"  <Horario>{ph.get('{{horario_brasilia}}', 'N/A')}</Horario>")
            system_parts.append(f"  <DiaSemana>{ph.get('{{dia_semana_brasilia}}', 'N/A')}</DiaSemana>")
            system_parts.append("  <Uso>Use para: saudação adequada (Bom dia / Boa tarde / Boa noite) e para function calls que dependam de data/hora.</Uso>")
            system_parts.append("</DataHoraAtual>")
        except Exception:
            pass  # não quebrar o fluxo se falhar
        
        # ESTRUTURA HIERÁRQUICA: ATENDIMENTO -> STATUS + MEMÓRIA
        # Iniciar estrutura XML do atendimento
        if attendance_id:
            system_parts.append("")
            system_parts.append("<Atendimento>")
            
            # STATUS DO ATENDIMENTO
            _state_labels = {
                "TRIAGEM": "em triagem (inicial)",
                "ABERTO": "aberto",
                "EM_ATENDIMENTO": "em atendimento (com vendedor)",
                "AGUARDANDO_CLIENTE": "aguardando resposta do cliente",
                "AGUARDANDO_VENDEDOR": "aguardando vendedor",
                "FECHADO_OPERACIONAL": "fechado",
            }
            _ctx_labels = {
                "novo": "Novo atendimento",
                "reaberto": "Atendimento reaberto (retomamos o anterior)",
                "em_andamento": "Conversa em andamento"
            }
            
            system_parts.append("  <Status>")
            if operational_state:
                label = _state_labels.get(operational_state, operational_state)
                system_parts.append(f"    <Estado>{label}</Estado>")
            else:
                system_parts.append("    <Estado>não especificado</Estado>")
            
            if attendance_context and attendance_context in _ctx_labels:
                system_parts.append(f"    <Contexto>{_ctx_labels[attendance_context]}</Contexto>")
            else:
                system_parts.append("    <Contexto>não especificado</Contexto>")
            
            system_parts.append("    <Instrucao>Use isso para decidir quando informar que retomamos o atendimento (reaberto), quando é novo ou em andamento. Não sugira 'abrir' atendimento se já estiver aberto ou em andamento.</Instrucao>")
            system_parts.append("  </Status>")
            
            # MEMÓRIA HIERÁRQUICA: RECENTES + RESUMO + RAG (dentro de Atendimento)
            system_parts.append("  <Memoria>")
            system_parts.append("    <Recentes tipo=\"curto-prazo\">")
            if recent_messages:
                system_parts.append("      <Conteudo>")
                for line in (recent_messages or "").strip().split("\n"):
                    system_parts.append("        " + line)
                system_parts.append("      </Conteudo>")
                system_parts.append("      <Descricao>Mensagens recentes da conversa atual (todas após a 10ª mensagem mais antiga do cliente)</Descricao>")
            else:
                system_parts.append("      <Conteudo>Primeiro contato do cliente.</Conteudo>")
                system_parts.append("      <Descricao>Nenhuma mensagem anterior no atendimento</Descricao>")
            system_parts.append("    </Recentes>")
            system_parts.append("    <Resumo tipo=\"medio-prazo\">")
            if intermediate_summary:
                system_parts.append("      <Conteudo>")
                for line in (intermediate_summary or "").strip().split("\n"):
                    system_parts.append("        " + line)
                system_parts.append("      </Conteudo>")
                system_parts.append("      <Descricao>Resumo das 30 mensagens do cliente antes da janela recente (gerado a cada 10 mensagens do cliente)</Descricao>")
            else:
                system_parts.append("      <Conteudo>Nenhum resumo disponível.</Conteudo>")
                system_parts.append("      <Descricao>Conversa ainda não possui histórico suficiente para resumo</Descricao>")
            system_parts.append("    </Resumo>")
            system_parts.append("    <RAG tipo=\"longo-prazo\">")
            if rag_context:
                system_parts.append("      <Conteudo>")
                for line in (rag_context or "").strip().split("\n"):
                    system_parts.append("        " + line)
                system_parts.append("      </Conteudo>")
                system_parts.append("      <Descricao>Contexto recuperado de até 100 atendimentos anteriores do cliente (acionado quando informação não está em Recentes nem em Resumo)</Descricao>")
            else:
                system_parts.append("      <Conteudo>Nenhum contexto histórico recuperado.</Conteudo>")
                system_parts.append("      <Descricao>Cliente não possui atendimentos anteriores relevantes ou informação já está em Recentes/Resumo</Descricao>")
            system_parts.append("    </RAG>")
            system_parts.append("  </Memoria>")
            if open_cases:
                system_parts.append("  <CasosAbertos>")
                for caso in open_cases:
                    type_label = caso.get('type_label', caso.get('type_key', ''))
                    title = caso.get('title') or 'Sem título'
                    status = caso.get('status', '')
                    system_parts.append(f"    <Caso tipo='{type_label}' status='{status}'>{title}</Caso>")
                system_parts.append("    <Descricao>Situações criadas por function calls que precisam de resposta humana (vendedor/supervisor)</Descricao>")
                system_parts.append("  </CasosAbertos>")
            system_parts.append("</Atendimento>")
        
        # Informações de triagem (quando atendimento está em TRIAGEM)
        if triagem_context:
            triagem_info = []
            
            # Verificar se tem atendimentos anteriores
            if triagem_context.get('has_previous_attendances'):
                closed_count = len(triagem_context.get('closed_attendances', []))
                triagem_info.append(f"O cliente possui {closed_count} atendimento(s) anterior(es) fechado(s).")
            
            # Verificar atendimentos em aberto
            if triagem_context.get('has_open_attendances'):
                open_attendances = triagem_context.get('open_attendances', [])
                triagem_info.append(f"\n⚠️ ATENÇÃO: O cliente possui {len(open_attendances)} atendimento(s) em aberto:")
                for att in open_attendances:
                    state = att.get('operational_state', 'DESCONHECIDO')
                    cases = att.get('open_cases', [])
                    incomplete = att.get('incomplete_process', False)
                    
                    info_line = f"- Atendimento #{att.get('id', 'N/A')[:8]}... (Estado: {state})"
                    if cases:
                        case_types = [c.get('type_label', c.get('type_key', '')) for c in cases]
                        info_line += f" com {len(cases)} caso(s) aberto(s): {', '.join(case_types)}"
                    if incomplete:
                        info_line += " [PROCESSO INCOMPLETO - cliente parou no meio]"
                    triagem_info.append(info_line)
            
            # Verificar últimos 3 atendimentos com casos abertos
            recent_with_cases = triagem_context.get('recent_with_open_cases', [])
            if recent_with_cases:
                triagem_info.append(f"\n📋 Nos últimos 3 atendimentos, há casos em aberto:")
                for att in recent_with_cases:
                    cases = att.get('open_cases', [])
                    case_types = [c.get('type_label', c.get('type_key', '')) for c in cases]
                    triagem_info.append(f"- Atendimento #{att.get('id', 'N/A')[:8]}...: {', '.join(case_types)}")
            
            # Verificar se parou no meio de algum processo
            if triagem_context.get('last_3_has_incomplete'):
                triagem_info.append(f"\n⏸️ O cliente parou no meio de algum processo em atendimento(s) anterior(es).")
            
            if triagem_info:
                system_parts.append("\n### CONTEXTO DE TRIAGEM - HISTÓRICO DO CLIENTE")
                system_parts.append("\n".join(triagem_info))
                system_parts.append("\n⚠️ IMPORTANTE: Use essas informações para perguntar ao cliente se ele quer:")
                system_parts.append("- Reabrir um atendimento antigo")
                system_parts.append("- Continuar um atendimento que está em aberto")
                system_parts.append("- Ou iniciar um novo atendimento")
                system_parts.append("Só pode haver UM atendimento ativo por cliente. Se o cliente escolher continuar/reabrir, você deve usar a function call apropriada para isso.")
        
        # Resumo do último atendimento (quando atendimento atual é novo e cliente tinha atendimentos finalizados)
        if last_attendance_summary and last_attendance_summary.strip():
            system_parts.append(f"\n### Resumo do último atendimento do cliente\n{last_attendance_summary.strip()}")
        
        # IMPORTANTE: Instruções sobre tags de mensagem (SEMPRE presente em todos os agentes) - formato XML
        # Esta seção deve estar presente em 100% dos prompts - informação do sistema
        system_parts.append("")
        system_parts.append("<Importante>")
        system_parts.append("  <Instrucao>As tags &lt;Text&gt;, &lt;Audio&gt;, &lt;Image&gt; indicam o tipo original da mensagem que você recebeu. INTERPRETE o conteúdo como se você tivesse realmente ouvido o áudio (quando marcado como &lt;Audio&gt;) ou visto a imagem (quando marcado como &lt;Image&gt;). NÃO trate como uma transcrição ou descrição - responda como se tivesse experiência direta com o conteúdo original.</Instrucao>")
        system_parts.append("  <Instrucao>Você NÃO deve retornar essas tags na sua resposta. Responda normalmente ou acione a ferramenta indicada pelos gatilhos do prompt.</Instrucao>")
        system_parts.append("</Importante>")

        # DadosParaOrcamento: variáveis dinâmicas (Capturados, UltimoEnviado) da FC pedido-orcamento
        if attendance_id:
            dados_orcamento_xml = await self.memory_manager.get_dados_para_orcamento_xml(attendance_id)
            if dados_orcamento_xml:
                system_parts.append(dados_orcamento_xml)

        return "\n".join(system_parts).strip()
    
